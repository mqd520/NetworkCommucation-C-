#pragma once
#include "TcpEvt.h"

namespace tc
{
	// 收到对端数据事件
	class RecvPeerDataEvt : public TcpEvt
	{
	public:
		//************************************
		// Method:    构造函数
		// Parameter: 关联的tcp服务对象
		// Parameter: 对端socket
		// Parameter: BYTE * pBuf:	缓冲区
		// Parameter: int len:		缓冲区长度
		//************************************
		RecvPeerDataEvt(TcpService* pSrv, SOCKET recv, BYTE* pBuf, int len);
		~RecvPeerDataEvt();

	private:
		BYTE* pBuf;		// 收到的数据缓冲区指针
		int nLen;		// 数据缓冲区长度

	public:
		//************************************
		// Method:    获取接收缓冲区
		//************************************
		BYTE* GetRecvBuf();

		//************************************
		// Method:    获取接收缓冲区长度
		//************************************
		int GetBufLen();
	};
}